export function createMaskPolygon(stateBoundary: StateBoundary): Array<Array<{ lat: number; lng: number }>> {
  const worldBounds = [
    { lat: 85, lng: -180 },
    { lat: 85, lng: 180 },
    { lat: -85, lng: 180 },
    { lat: -85, lng: -180 },
    { lat: 85, lng: -180 },
  ];

  const stateHole = [...stateBoundary.coordinates].reverse();

  return [worldBounds, stateHole];
}